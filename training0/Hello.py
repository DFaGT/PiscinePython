# 修正後のコード例

# リスト: "Hello" と "World!" に変更
ft_list = ["Hello", "World!"]

# タプル: "Hello" と "France!" に変更
ft_tuple = ("Hello", "France!")

# セット: "Hello" と "Paris!" に変更
ft_set = {"Hello", "Paris!"}

# 辞書: キー "Hello" に対して "42Paris!" に変更
ft_dict = {"Hello": "42Paris!"}

# 結果を出力
print(ft_list)
print(ft_tuple)
print(ft_set)
print(ft_dict)
